public class Statement_While {
    public static void main(String[] args) {
        int j = 1;
        
        while(j<11){
            // blok kode yang akan diulang
            System.out.println("Perulangan ke-" +j);
            // increment
            j++;
        }
        // jika nilai j menyentuh di angka 11 maka perulangan akan berhenti
    }
}
